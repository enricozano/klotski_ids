package klotski_ids;

/**
 * The Launcher class that contains the main method to launch the application.
 */
public class Launcher {

    /**
     * The main method that launches the application by calling the main method of the mainApplication class.
     *
     * @param args the command-line arguments.
     */
    public static void main(String[] args) {
        mainApplication.main(args);
    }
}
